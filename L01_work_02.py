# 2. Выполнить логические побитовые операции «И», «ИЛИ» и др. над числами 5 и 6. Выполнить над числом 5 побитовый сдвиг вправо и влево на два знака. Объяснить полученный результат.

print('Подитовое И для чисел 5 и 6 =', 5 & 6)
print('Побитовое ИЛИ для чисел 5 и 6 =', 5 | 6)
print('Побитовое ИСКЛЮЧАЮЩЕЕ ИЛИ для чисел 5 и 6 =', 5 ^ 6)
print('Побитовое сдвиг влево 5 на 2 знака =', 5 << 2)
print('Побитовое сдвиг вправо 5 на 2 знака =', 5 >> 2)
